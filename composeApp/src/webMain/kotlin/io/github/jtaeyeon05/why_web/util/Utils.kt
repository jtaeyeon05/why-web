}

operator fun URLSearchParams.get(name: String): String? = getEncoded(name)?.let { decodeURIComponent(it) }

fun Random.boxMuller(
    mean: Double = 0.0,
    stdDev: Double = 1.0,
): Pair<Double, Double> {
    require(stdDev > 0.0) { "stdDev must be > 0" }

    var u1: Double
    do {
        u1 = nextDouble()
    } while (u1 <= 0.0)
    val u2 = nextDouble()

    val r = sqrt(-2.0 * ln(u1))
    val theta = 2 * PI * u2

    val z0 = r * sin(theta)
    val z1 = r * cos(theta)

    return z0 * stdDev + mean to z1 * stdDev + mean
}

fun Random.nextGaussian(
    mean: Double = 0.0,
    stdDev: Double = 1.0,
    min: Double = -Double.MAX_VALUE,
    max: Double = +Double.MAX_VALUE,
    n: Int = 100,
): Double {
    repeat(n) {
        val (z0, z1) = boxMuller(mean, stdDev)
        if (z0 in min .. max) return z0
        if (z1 in min .. max) return z1
    }
    return nextDouble(min, max)
}
